package com.peas.model;

public class Peca {
	
	int x;
	int y;
	boolean rainha;
	boolean inimiga;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public boolean isRainha() {
		return rainha;
	}

	public void setRainha(boolean rainha) {
		this.rainha = rainha;
	}

	public boolean isInimiga() {
		return inimiga;
	}

	public void setInimiga(boolean inimiga) {
		this.inimiga = inimiga;
	}

}
